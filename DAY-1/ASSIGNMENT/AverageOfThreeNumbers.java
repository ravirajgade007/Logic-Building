class AverageOfThreeNumbers
   {
     public static void main(String args[])
      {
           int num1 = 20;
           int num2 = 40;
           int num3 = 60;
            
           double average = (num1 + num2 + num3) / 3.0;
         System.out.println("Average Of Three Numbers: " + average);
     }
}
