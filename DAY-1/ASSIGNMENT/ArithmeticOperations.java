class ArithmeticOperations
{
   public static void main(String args[])
     {
        int num1 = -5 + (8*6);
        int num2 = (55+9) % 9;
        int num3 = 20 + (-3*5) / 8;
        int num4 = 5 + (15 / 3) * 2 - (8%3);

       System.out.println(num1);
       System.out.println(num2);
       System.out.println(num3);
       System.out.println(num4); 
     }
}