class BasicArithmeticOperations
     {
       public static void main(String args[])
        {
            int num1 = 125;
            int num2 = 24;
    
             int addition = num1 + num2;
             System.out.println("Addition Of Two Numbers are : " + addition);

	     int substraction = num1 - num2;
             System.out.println("Substraction Of Two Numbers are : " + substraction);

             int multiplication = num1 * num2;
             System.out.println("Multiplication Of Two Numbers are : " + multiplication);

             int division = num1 / num2;
             System.out.println("Division Of Two Numbers are : " + division);
      }
}